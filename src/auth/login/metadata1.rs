#[cfg(test)]
fn xxtea_decrypt(v: &mut [u32], key: &[u32; 4]) {
    let n = v.len();
    if n < 2 {
        return;
    }
    let rounds = 6 + 52 / n;
    let mut sum = (rounds as u32).wrapping_mul(DELTA);
    let mut y = v[0];
    while sum != 0 {
        let e = (sum >> 2) & 3;
        for p in (1..n).rev() {
            let z = v[p - 1];
            v[p] = v[p].wrapping_sub(mx(z, y, sum, p, e, key));
            y = v[p];
        }
        let z = v[n - 1];
        v[0] = v[0].wrapping_sub(mx(z, y, sum, 0, e, key));
        y = v[0];
        sum = sum.wrapping_sub(DELTA);
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn crc32_matches_zlib() {
        assert_eq!(crc32(b"audible-rs"), 0xDC82_FBB8);
        assert_eq!(crc32(b""), 0);
    }

    #[test]
    fn encrypt_metadata_matches_reference_vector() {
        // Cross-checked against the Python `audible` metadata1 (whose key +
        // algorithm decrypt a live, Amazon-accepted value).
        assert_eq!(
            encrypt_metadata("audible-rs"),
            "ECdITeCs:yGBVzisZJvw/L1u7ZYdWgurfLTU="
        );
        assert_eq!(encrypt_metadata("x"), "ECdITeCs:Oo8+0npaF9M+CLRR");
    }

    #[test]
    fn xxtea_round_trips() {
        let plain = "the quick brown fox jumps over the lazy dog 1234";
        let mut longs = bytes_to_longs(plain.as_bytes());
        xxtea_encrypt(&mut longs, &KEY);
        xxtea_decrypt(&mut longs, &KEY);
        let mut out = longs_to_bytes(&longs);
        while out.last() == Some(&0) {
            out.pop();
        }
        assert_eq!(out, plain.as_bytes());
    }

    #[test]
    fn meta_audible_app_is_valid_json_with_inputs() {
        let json = meta_audible_app("UA/1.0", "https://www.amazon.de/ap/signin?x=1");
        let value: serde_json::Value = serde_json::from_str(&json).unwrap();
        assert_eq!(value["userAgent"], "UA/1.0");
        assert_eq!(value["location"], "https://www.amazon.de/ap/signin?x=1");
        assert_eq!(value["version"], "3.0.0");
        // Encrypting the template yields the prefixed form.
        assert!(encrypt_metadata(&json).starts_with("ECdITeCs:"));
    }
}
