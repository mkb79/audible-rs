//! `metadata1` for the scripted login — the encrypted device-context field
//! Amazon's sign-in form expects. It is XXTEA (fixed 128-bit key) over
//! `CRC32_HEX#json`, base64'd with an `ECdITeCs:` prefix. Key and algorithm
//! are verified against a live captured value. The field is optional (the form
//! also accepts none / `false`), but including it reduces captcha frequency.

use base64::Engine as _;
use base64::engine::general_purpose::STANDARD as BASE64;

/// 128-bit XXTEA key (`struct.unpack("IIII", METADATA_KEY)`, little-endian).
const KEY: [u32; 4] = [
    u32::from_le_bytes([0x61, 0x03, 0x8f, 0x70]),
    u32::from_le_bytes([0x34, 0x18, 0x97, 0x99]),
    u32::from_le_bytes([0x3a, 0xeb, 0xe7, 0x8b]),
    u32::from_le_bytes([0x85, 0x97, 0x24, 0x34]),
];

const DELTA: u32 = 0x9E37_79B9;

fn mx(z: u32, y: u32, sum: u32, p: usize, e: u32, key: &[u32; 4]) -> u32 {
    (((z >> 5) ^ (y << 2)).wrapping_add((y >> 3) ^ (z << 4)))
        ^ ((sum ^ y).wrapping_add(key[((p as u32 & 3) ^ e) as usize] ^ z))
}

/// XXTEA encrypt, in place (mod-2^32 arithmetic; matches the reference's
/// outer-only `u32()` wrapping because XOR/low-32-bit identities hold).
fn xxtea_encrypt(v: &mut [u32], key: &[u32; 4]) {
    let n = v.len();
    if n < 2 {
        return;
    }
    let rounds = 6 + 52 / n;
    let mut sum: u32 = 0;
    let mut z = v[n - 1];
    for _ in 0..rounds {
        sum = sum.wrapping_add(DELTA);
        let e = (sum >> 2) & 3;
        for p in 0..n - 1 {
            let y = v[p + 1];
            v[p] = v[p].wrapping_add(mx(z, y, sum, p, e, key));
            z = v[p];
        }
        let y = v[0];
        v[n - 1] = v[n - 1].wrapping_add(mx(z, y, sum, n - 1, e, key));
        z = v[n - 1];
    }
}

fn bytes_to_longs(data: &[u8]) -> Vec<u32> {
    data.chunks(4)
        .map(|chunk| {
            let mut buf = [0u8; 4];
            buf[..chunk.len()].copy_from_slice(chunk);
            u32::from_le_bytes(buf)
        })
        .collect()
}

fn longs_to_bytes(v: &[u32]) -> Vec<u8> {
    v.iter().flat_map(|x| x.to_le_bytes()).collect()
}

/// CRC-32 (IEEE / zlib / `binascii.crc32`).
fn crc32(data: &[u8]) -> u32 {
    let mut crc: u32 = 0xFFFF_FFFF;
    for &byte in data {
        crc ^= byte as u32;
        for _ in 0..8 {
            crc = if crc & 1 != 0 {
                (crc >> 1) ^ 0xEDB8_8320
            } else {
                crc >> 1
            };
        }
    }
    !crc
}

/// Encrypts a metadata JSON string into the `ECdITeCs:`-prefixed form value.
pub(crate) fn encrypt_metadata(metadata: &str) -> String {
    let object = format!("{:08X}#{metadata}", crc32(metadata.as_bytes()));
    let mut longs = bytes_to_longs(object.as_bytes());
    xxtea_encrypt(&mut longs, &KEY);
    format!("ECdITeCs:{}", BASE64.encode(longs_to_bytes(&longs)))
}

fn now_unix_ms() -> i64 {
    std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .map(|d| d.as_millis() as i64)
        .unwrap_or(0)
}

